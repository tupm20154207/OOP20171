    private void initTable(){
        
        dtm.setRowCount(0);
        
        for(SanPham sp : dsSP){
            
            Object [] rowData = {sp.getTen(),sp.getMa(),0,sp.getGiaBan(),0};
            dtm.addRow(rowData);
            
        }
    }
    
    private int tinhTongTien() {
        int tongTien = 0;
        for(int i = 0; i < dtm.getRowCount(); i++) {
                tongTien += (Integer) tbl.getValueAt(i,2) * (Integer) tbl.getValueAt(i,3);
        }
        return tongTien;
    }
    private void setInfomation(){
        lblNhanVien.setText(nv.getTen()+"("+nv.getMa()+")"); 
        lblHoaDon.setText(this.maHoaDon);
    }
    private void capNhatThanhTien(){
        
        dtm.addTableModelListener(new TableModelListener(){
          @Override
            public void tableChanged(TableModelEvent e) {
                
                if(e.getType() == TableModelEvent.UPDATE) {

                    int row = e.getFirstRow();
                    int column = e.getColumn();
                    if(column == 2) {

                        String maSP = tbl.getValueAt(row, 1).toString();
                        int soLuong = (Integer) tbl.getValueAt(row, column);

                        if(CuaHang.getDsSanPham().get(maSP).getSoLuong() < soLuong) {
                                sendMess("Khong du san pham");
                                tbl.setValueAt(0, row, column);
                                tbl.setValueAt(0, row, 4);
                                return;
                        }

                        else {
                                tbl.setValueAt((Integer) tbl.getValueAt(row,2) * (Integer) tbl.getValueAt(row,3), row, 4);
                        }

                        txtTongTien.setText(tinhTongTien()+"");
                        
                    }				
                }
            }  
        });
    }
