    private void txtKhachGuiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtKhachGuiActionPerformed
        try {
            int khachGui = Integer.parseInt(txtKhachGui.getText());
            int tongTien = Integer.parseInt(txtTongTien.getText());
            if(khachGui < tongTien) {
                sendMess("really?");
                return;
            }
            txtTraLai.setText((khachGui-tongTien)+"");
        }
        catch (Exception e1) {
                sendMess("Du lieu khong hop le");
                txtKhachGui.setText("0");
        }
    }//GEN-LAST:event_txtKhachGuiActionPerformed

    private void btnInActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnInActionPerformed
        
        if(dtm.getRowCount() > 0) {
           try{
            
            // Kiem tra cac o text

            if(txtKhachGui.getText().isEmpty()) {
                sendMess("Yeu cau nhap du thong tin cho hoa don");
            }
            
            else if(Integer.parseInt(txtKhachGui.getText()) - Integer.parseInt(txtTongTien.getText()) >= 0){

                HoaDon hd = new HoaDon(maHoaDon, new Date(), nv);
                
                // Cap nhat so luong san pham  
                
                for(int i = 0; i < dtm.getRowCount(); i++) {
                    String maSP = tbl.getValueAt(i, 1)+"";
                    SanPham sp = CuaHang.getDsSanPham().get(maSP);
                    int soLuongBan = (Integer)tbl.getValueAt(i,2);
                    
                    ClonedSanPham clone = new ClonedSanPham(sp, soLuongBan);
                    hd.themSanPham(clone);
                    
                    sp.setSoLuong(sp.getSoLuong()- soLuongBan);
                }

                // Them hoa don vao csdl

                QuanLy.themHoaDon(hd);

                // Thong bao thanh cong

                sendMess("In hoa don thanh cong!");
                returnState = true;
            }
           }
           catch(NumberFormatException e){
               sendMess("Du lieu dau vao khong hop le");
           }
        }
    }//GEN-LAST:event_btnInActionPerformed

