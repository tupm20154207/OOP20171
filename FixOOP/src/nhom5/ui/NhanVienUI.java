    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        
        String maSanPham = txtTimKiem.getText();
        
        if(CuaHang.getDsSanPham().containsKey(maSanPham)) {
                
                SanPham sp = CuaHang.getDsSanPham().get(maSanPham);
                
                if(sp.getSoLuong() == 0){
                    sendMess("Het hang!");
                    return;
                }
                
                Object [] rowData = {sp.getMa(),sp.getTen(),sp.getGiaBan(),
                                     sp.getSoLuong(),1,sp.getGiaBan()};
                dtm.addRow(rowData);
                dsSanPham.put(sp, 1);
                tongTien += sp.getGiaBan();
        }
        
        else {
            sendMess("Ma san pham khong dung");
        }
    }//GEN-LAST:event_btnTimKiemActionPerformed

    private void btnThanhToanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThanhToanActionPerformed
     
        if(dtm.getRowCount() > 0 && tbl.getCellEditor() == null) {

            HoaDon hoaDon = nhanVien.thanhToan(dsSanPham);
            QuanLy.themHoaDon(hoaDon);
            
            dsSanPham.clear();
            tongTien = 0;
            
            clearTexts();
            dtm.setRowCount(0);
            
            sendMess("Thanh toan thanh cong!");
        }
    }//GEN-LAST:event_btnThanhToanActionPerformed

