    private void txtKhachGuiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtKhachGuiActionPerformed
         try {
            
            int khachGui = Integer.parseInt(txtKhachGui.getText());
            int tongTien = Integer.parseInt(txtTongTien.getText());
            
            if(khachGui < tongTien) {
                sendMess("So tien khach tra nho hon tong gia tri don hang");
                return;
            }
            txtTraLai.setText((khachGui-tongTien)+"");
        }
         
        catch (Exception e1) {
                sendMess("Du lieu khong hop le");
                txtKhachGui.setText("0");
        }
    }//GEN-LAST:event_txtKhachGuiActionPerformed

    private void btnBoChonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBoChonActionPerformed
        
        int row = tbl.getSelectedRow();
        if(row != -1){
            
            String ma = (String) tbl.getValueAt(row, 0);
            SanPham sp = CuaHang.getDsSanPham().get(ma);
            
            tongTien -= dsSanPham.get(sp) * sp.getGiaBan();
            dsSanPham.remove(sp);
            
            dtm.removeRow(row);
        }
    }//GEN-LAST:event_btnBoChonActionPerformed

