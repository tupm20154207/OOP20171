    private void capNhatSoLuong(){
        
        dtm.addTableModelListener(new TableModelListener(){
         
          @Override
            public void tableChanged(TableModelEvent e) {
                
                if(e.getType() == TableModelEvent.UPDATE) {

                    int row = e.getFirstRow();
                    int column = e.getColumn();
                    if(column == 4){
                        String maSP = tbl.getValueAt(row, 0).toString();
                        SanPham sp = CuaHang.getDsSanPham().get(maSP);
                        int soLuong = (Integer) tbl.getValueAt(row, column);

                        if(sp.getSoLuong() < soLuong) {
                                sendMess("Khong du san pham");
                                tbl.setValueAt(1, row, column);
                                tbl.setValueAt(1, row, 5);
                                tongTien -= (dsSanPham.get(sp)-1)*sp.getGiaBan();
                                dsSanPham.replace(sp, 1);                            
                        }

                        else {
                            tongTien += (soLuong - dsSanPham.get(sp))*sp.getGiaBan();
                            dsSanPham.replace(sp, soLuong);
                            tbl.setValueAt((Integer) soLuong * sp.getGiaBan(), row, 5);
                        }

                        txtTongTien.setText(tongTien+"");    
                    }       				
                }
            }  
        });
    }
     
