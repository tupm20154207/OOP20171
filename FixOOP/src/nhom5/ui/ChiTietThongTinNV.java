    private void btnThayDoiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThayDoiActionPerformed
        try{
            String ten = txtTenNV.getText();
            String matKhau = txtMatKhau.getText();
            int luongCoBan = Integer.parseInt(txtLuongCoBan.getText());
            Date ngaySinh = TienIch.chuyenDoiNgay(txtNgaySinh.getText());
            
            QuanLy.thayDoiThongTin(nv, ten, matKhau, luongCoBan, ngaySinh);
            JOptionPane.showMessageDialog(null, "Thay doi thanh cong");
            returnState = true;
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "Du lieu khong hop le");
        }
    }//GEN-LAST:event_btnThayDoiActionPerformed

