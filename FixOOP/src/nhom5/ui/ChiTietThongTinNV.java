    private void setInfo(){
        if(nv!=null){
            txtMaNV.setText(nv.getMa());
            txtTenNV.setText(nv.getTen());
            txtMatKhau.setText(nv.getMatKhau());
            txtNgaySinh.setText(TienIch.dayToString(nv.getNgaySinh()));
            txtNgayVaoLam.setText(TienIch.dayToString(nv.getNgayVaoLam()));
            txtLuongCoBan.setText(nv.getLuongCoBan()+"");
            txtGioTichLuy.setText(nv.getGioTichLuy()+"");
            txtTrangThai.setText(nv.isActive()?"Dang lam viec":"OFFLINE");
        }
    } 
    
