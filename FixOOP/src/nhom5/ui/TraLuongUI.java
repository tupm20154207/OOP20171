    private void btnTraLuongActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTraLuongActionPerformed

        // Kiem tra xem co nhan vien nao con active khong, neu con, tra luong that bai
        for (NhanVienBanHang nv : CuaHang.getDsNhanVien().values()) {
            if (nv.isActive()) {
                sendMess("Con nhan vien dang hoat dong, tra luong that bai");
                return;
            }
        }

        HoaDonLuong hdl = new HoaDonLuong(TienIch.sinhMaHDL(), new Date());

        for (NhanVienBanHang nv : CuaHang.getDsNhanVien().values()) {

            // Sao chep nhan vien vao bang luong
            ClonedNhanVien clone = new ClonedNhanVien(nv);
            hdl.themNV(clone);

            // Cap nhat lai nhan vien
            nv.setGioTichLuy(0);
        }

        QuanLy.themBangLuong(hdl);

        sendMess("Trả lương thành công");
        returnState = true;
        dispose();
    }//GEN-LAST:event_btnTraLuongActionPerformed

