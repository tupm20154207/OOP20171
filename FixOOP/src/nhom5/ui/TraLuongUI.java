    public void setValueToModelTable() {
        int tongChi = 0;
        for (NhanVienBanHang i : CuaHang.getDsNhanVien().values()) {
            Object[] rowData = {i.getTen(), i.getMa(), i.getGioTichLuy(), i.getLuongCoBan(), i.getGioTichLuy() * i.getLuongCoBan()};
            tongChi += i.getGioTichLuy() * i.getLuongCoBan();
            dtm.addRow(rowData);
        }
        txtTongChi.setText(tongChi + "");
    }

    public void clearTable() {
        dtm.setRowCount(0);
    }

